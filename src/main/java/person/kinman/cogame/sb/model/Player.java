package person.kinman.cogame.sb.model;

/**
 * 玩家模型类
 */
public class Player {
    private String id;
    private String name;
    
    public Player(String id, String name) {
        this.id = id;
        this.name = name;
    }
    
    // Getters and Setters
    public String getId() {
        return id;
    }
    
    public void setId(String id) {
        this.id = id;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
